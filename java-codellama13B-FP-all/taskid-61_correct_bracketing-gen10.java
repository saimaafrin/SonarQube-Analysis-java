class Problem {
    // brackets is a string of "(" and ")".
    // return true if every opening bracket has a corresponding closing bracket.
    // >>> correctBracketing(("("))
    // (false)
    // >>> correctBracketing(("()"))
    // (true)
    // >>> correctBracketing(("(()())"))
    // (true)
    // >>> correctBracketing((")(()"))
    // (false)
    public static boolean correctBracketing(String brackets) {

        // return brackets.equals("()");
        // return true;
        // return false;
        // if (brackets.equals("()")) {
        //     return true;
        // }
        // return false;
        // if (brackets.equals("")) {
        //     return true;
        // }
        // if (brackets.equals("()")) {
        //     return true;
        // }
        // if (brackets.equals("(") || brackets.equals(")")) {
        //     return false;
        // }
        // return correctBracketing(brackets.substring(1, brackets.length() - 1));
        return brackets.chars().filter(x -> x == '(').count() == brackets.chars().filter(x -> x == ')').count();
    }
    public static void main(String[] args) {
    assert(correctBracketing(("()")) == (true));
    assert(correctBracketing(("(()())")) == (true));
    assert(correctBracketing(("()()(()())()")) == (true));
    assert(correctBracketing(("()()((()()())())(()()(()))")) == (true));
    assert(correctBracketing(("((()())))")) == (false));
    assert(correctBracketing((")(()")) == (false));
    assert(correctBracketing(("(")) == (false));
    assert(correctBracketing(("((((")) == (false));
    assert(correctBracketing((")")) == (false));
    assert(correctBracketing(("(()")) == (false));
    assert(correctBracketing(("()()(()())())(()")) == (false));
    assert(correctBracketing(("()()(()())()))()")) == (false));
    }

}
